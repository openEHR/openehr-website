				<p>Xudong Lu is professor of biomedical informatics in Zhejiang University of China, associate director of the Biomedical Information Technology Institute of ZJU. He also serves as General Secretary of the Chinese Medical Software Association which has large influence on health IT domain in China.</p>
				<p>Xudong has been involved in healthcare informatics for over 20 years and led several major Chinese national projects including "High-end EMR Technologies and System Development" and "National Registy of Cadiovascular Diseases".  He is a visiting guest professor at the Technical University of Eindhoven, where he co-supervised several PhD students on clinical pathway analytics and clinical decision support.</p>
				<p>His research interests are medical informatics, clinical decision support systems and big data analytics in medicine.</p>
