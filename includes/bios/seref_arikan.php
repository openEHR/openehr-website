					<p>Seref Arikan is a software architect who has been working at Ocean Informatics UK since 2010. He started his professional career in 1997 as a freelance developer working on B2B and B2C solutions and has worked in various health IT companies since 2001, mostly in various R&D and product development roles.</p>
					<p>His work on openEHR started around 2002, while he was working for a hospital information system vendor. He has released various openEHR related software components and tools as open source in 2009, which were develop in relation to his PhD in medical informatics. Seref's PhD, which he completed at UCL, CHIME, under the supervision of Professor David Ingram, is on the integration of openEHR specifications with Bayesian Networks for clinical decision support.</p>

					<p>He is currently in charge of development of various platform components at Ocean Informatics, with a focus on analytics, scalability and API development. His particular interest and most active research is on the implementation of openEHR persistence and relevant specifications such as AQL.</p>
