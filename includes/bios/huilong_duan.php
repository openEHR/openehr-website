				<p>Huilong Duan is professor of biomedical informatics in Zhejiang University of China. He was appointed to the Chair of China Medical Software Association since 2014, Vice Chair of China Smart &amp; Mobile Health Association since 2014, Vice Chair of China Ministry of Education of Biomedical Engineering Teaching Steering Committee since 2006.</p>
				<p>Huilong Duan started research on biomedical informatics since 1995, current research interests include medical information integration, healthcare big data analytics, medical knowledge management and clinical decision support. He led several national research projects including "medical information integration &amp; clinical decision support system" since 2006, "medical information integration &amp; fusion technology" since 2012, "Big Data Standardization in Precision Medicine" since 2016. His team has initiated openEHR-related research since 2011 and implemented the first pilot openEHR-based CDR system in a hospital in China in 2013.</p>
