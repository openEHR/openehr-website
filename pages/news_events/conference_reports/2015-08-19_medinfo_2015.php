		<p>High points for me were the <a href="https://openehr.atlassian.net/wiki/display/resources/MEDINFO+2015+-+Sao+Paulo%2C+Brazil">presentations</a>, from left field as it were, given by innovative people, who had come to openEHR via the web, got to understand it and how to use the openEHR methodology, arriving on the Medinfo programme, completely unbeknown to any of us, to report their progress. Prof Xudong Lu’s paper on the use and adaptation of archetypes to meet the well-rehearsed and researched requirements for clinical records for his 2500 bed hospital at Zheijang University in Hangzhou, China, was one such quite breath-taking example. There were numerous others.</p>

