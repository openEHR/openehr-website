		<p>For our community, it was a triumph of talent, cooperation and inventiveness, and a great opportunity for many of our members pushing for openEHR adoption to meet for the first time, compare notes and socialise with one another and with interested conference delegates. The community spirit, and, dare I say it, its youthfulness, had a great impact on the meeting and openEHR was visited and mentioned throughout the conference. It certainly had a big impact on me.</p>
	
