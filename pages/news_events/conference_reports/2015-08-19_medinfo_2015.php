		<h2>Some reflections from Prof David Ingram, President and Chair of the openEHR Foundation Board of Governors</h2>

		<a href="/files/news_events/conferences/poster_1_medinfo_2015.png"> <img src="/files/news_events/conferences/poster_1_medinfo_2015.png" alt="openEHR MedInfo 2015" title="Click to enlarge" width="760"/></a>

		<p>There was, for sure, as anyone rehearsed in the songs of Frank Sinatra would expect, an awful lot of coffee in Brazil. But, exceeding our expectations, perhaps, there was also an awful lot of openEHR as well, at Medinfo2015 in Sao Paulo - in presentations, tutorials, workshops and posters. The exhibition stand, ably brought together by our Management Board and Industry Partners, was well designed and softly stated, capturing the spirit of openEHR in a simple banner – openEHR – An Open, Vendor-Neutral eHealth Data Platform. </p>

