		<p>We kept coming back to the stand to regroup and attend to new visitors – one minute a conversation about applying openEHR in a 300 centre neurology network in Russia, then half an hour of reminiscence with a conference keynote speaker about the progress of his health data bank initiative, internationally, then shaking hands and talking with a vice-president of one of the major US vendors, keen to explore how they might get involved … and so it went on from Tuesday to Sunday.</p>

